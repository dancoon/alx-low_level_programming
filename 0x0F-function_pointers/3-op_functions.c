#include "3-calc.h"
/**
 * op_add - sums a and b
 * @a: first parameter
 * @b: second parameter
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - difference of a and b
 * @a: first parameter
 * @b: second parameter
 * Return: difference
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies a and b
 * @a: first parameter
 * @b: second paameter
 * Return: product
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divides two numbers
 * @a: first parameter
 * @b: second parameter
 * Return: quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulo division
 * @a: first parameter
 * @b: second parameter
 * Return: (a % b)
 */
int op_mod(int a, int b)
{
	return (a % b);
}

